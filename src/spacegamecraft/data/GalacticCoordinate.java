package spacegamecraft.data;

public class GalacticCoordinate {
	public int galaxyx;
	public int galaxyy;
	public int chunkx;
	public int chunky;
	public GalacticCoordinate(int galaxyx, int galaxyy, int chunkx, int chunky) {
		this.galaxyx = galaxyx;
		this.galaxyy = galaxyy;
		this.chunkx = chunkx;
		this.chunky = chunky;
	}
}
